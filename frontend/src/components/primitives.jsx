export function formatDetectionTimestamp(detection) {
  return detection?.timestamp
    ? new Date(detection.timestamp).toLocaleString()
    : detection?.detected || new Date().toLocaleString();
}

export function getEvidenceReportData(detection, asset) {
  const generatedAt = new Date().toLocaleString();
  const observedAt = formatDetectionTimestamp(detection);
  const evidenceText = Array.isArray(detection?.evidence)
    ? detection.evidence.filter(Boolean)
    : detection?.evidence
      ? [detection.evidence]
      : [];
  const transformations = Array.isArray(detection?.transformations)
    ? detection.transformations.filter(Boolean)
    : detection?.transformations
      ? [detection.transformations]
      : detection?.mod
        ? [detection.mod]
        : [];

  return {
    generatedAt,
    observedAt,
    detectionId: detection?.id || 'pending-review',
    status: (detection?.type || 'review').toUpperCase(),
    verdict: detection?.geminiVerdict || 'UNKNOWN',
    confidence: detection?.confidence ?? 0,
    phashSimilarity: detection?.phashSim ?? 0,
    title: asset?.title || 'Unknown asset',
    owner: asset?.owner || 'Unknown owner',
    sport: asset?.sport || 'Unknown sport',
    license: asset?.license || 'Unspecified',
    fingerprint: asset?.phash || 'Unavailable',
    frameRef: asset?.frame || 'N/A',
    suspectUrl: detection?.url || 'Unavailable',
    modificationNotes: detection?.mod || 'None recorded',
    reasoning: detection?.reasoning || 'No narrative reasoning captured in this session.',
    evidence: evidenceText.length ? evidenceText : ['No structured evidence captured.'],
    transformations: transformations.length ? transformations : ['Not specified'],
  };
}

